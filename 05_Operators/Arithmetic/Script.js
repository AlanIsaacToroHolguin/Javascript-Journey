// === Arithmetic Operators in JavaScript ===

// Arithmetic operators are used to perform mathematical operations between numbers.

// Examples:

let numero1 = 56;
let numero2 = 44;

let suma = numero1 + numero2;          // Addition
let resta = numero1 - numero2;         // Subtraction
let multi = numero1 * numero2;         // Multiplication
let division = numero1 / numero2;      // Division
let resto = numero1 % numero2;         // Modulus (remainder)
let potencia = numero1 ** 2;            // Exponentiation (numero1 raised to the power of 2)

// Output results to console:
console.log("Suma: " + suma);
console.log("Resta: " + resta);
console.log("Multiplicación: " + multi);
console.log("División: " + division);
console.log("Resto: " + resto);
console.log("Potencia: " + potencia);

/*
  Summary:
  +  : Adds two numbers.
  -  : Subtracts the second number from the first.
  *  : Multiplies two numbers.
  /  : Divides the first number by the second.
  %  : Returns the remainder of the division.
  ** : Raises the first number to the power of the second.
*/
