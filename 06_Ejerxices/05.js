/*
Exercise 5:

I have a cheesecake with 16 slices.  
Each slice costs 3.8 euros.  
If I sell all the slices, how much money do I earn in total?  
Store the result in a variable and display it in the console.
*/

// Data
const totalSlices = 16;
const pricePerSlice = 3.8;

// Calculation
const totalEarnings = totalSlices * pricePerSlice;

// Output
console.log(`If I sell all ${totalSlices} slices, I earn ${totalEarnings} euros in total.`);
