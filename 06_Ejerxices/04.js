/*
Exercise 4:

We have a giraffe in the zoo that weighs 1120 kilos.  
We feed it 141 kilos of fresh leaves.  
How much does the giraffe weigh now?  
Store the result in a variable and display it in the console.
*/

// Data
const initialWeight = 1120;
const food = 141;

// Calculation
const finalWeight = initialWeight + food;

// Output
console.log(`The giraffe now weighs ${finalWeight} kilos.`);

