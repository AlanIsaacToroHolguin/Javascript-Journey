/*
Exercise 2:

We have 7 boxes with 8 cookies each.  
How many cookies are there in total?  
Store the result in a variable and display it in the console.
*/

// Data
const numberOfBoxes = 7;
const cookiesPerBox = 8;

// Calculation
const totalCookies = numberOfBoxes * cookiesPerBox;

// Output
console.log(`The total number of cookies is: ${totalCookies}`);
